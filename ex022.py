nome = str(input('digite um nome:')).strip()
print('esse nome em letras maiúsculas fica assim: {}'.format(nome.upper()))
print('esse nome em letras minúsculas fica assim: {}'.format(nome.lower()))
print('esse nome tem {} letras'.format(len(nome) - nome.count(' ')))
print('a primeira palavra do nome tem {} letras'.format(nome.find(' ')))






